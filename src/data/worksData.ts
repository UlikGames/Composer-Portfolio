const applyB2Audio = (items: Work[]): Work[] =>
  items.map(work => ({
    ...work,
    audioUrl: b2Audio(work.audioUrl),
    movements: work.movements?.map(movement => ({
      ...movement,
      audioUrl: b2Audio(movement.audioUrl),
    })),
  }));

export const works: Work[] = applyB2Audio(worksData);

// Helper functions
export const getWorkById = (id: string): Work | undefined => {
  return works.find(work => work.id === id);
};

export const getInstrumentationFilters = (): string[] => {
  const instrumentations = new Set<string>();
  works.forEach(work => {
    work.instrumentation.forEach(inst => instrumentations.add(inst));
  });
  return Array.from(instrumentations).sort();
};

export const getTagFilters = (): string[] => {
  const tags = new Set<string>();
  works.forEach(work => {
    work.tags.forEach(tag => tags.add(tag));
  });
  return Array.from(tags).sort();
};
